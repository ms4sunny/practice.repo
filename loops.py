count = 1
while count <= 9:  # using while loop to iterate until count is less than or equal to 5
    print(count)  # using print function to display the current value of count
    count += 1  # incrementing count by 1 in each iteration
i = 10
while i >= 1:  # using while loop to iterate until i is greater than or equal to 1
    print(i)  # using print function to display the current value of i
    i -= 1  # decrementing i by 1 in each iteration
x = 3
while x <= 30:  # using while loop to iterate until x is less than or equal to 30
    if x % 3 == 0:  # checking if x is divisible by 3
        print(x)  # using print function to display the current value of x
    x += 1  # incrementing x by 1 in each iteration
y = 1
while y <= 10:  # using while loop to iterate until y is less than or equal to 10
    print(4*y)  # using print function to display the multiplication table of 2
